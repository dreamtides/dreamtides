/**
 * Integration test: loads the real card-data.json (generated by setup-assets)
 * and verifies the loader produces a correct database from actual card data.
 * Uses dynamic imports for Node.js fs/path to avoid browser tsconfig errors.
 */
/* eslint-disable @typescript-eslint/no-unsafe-assignment, @typescript-eslint/no-unsafe-call, @typescript-eslint/no-unsafe-member-access, @typescript-eslint/no-unsafe-argument */
async function readCardDataJson(): Promise<CardData[] | null> {
  try {
    // @ts-expect-error -- Node.js modules available at runtime in Vitest
    const fs = await import("node:fs");
    // @ts-expect-error -- Node.js modules available at runtime in Vitest
    const url = await import("node:url");
    // @ts-expect-error -- Node.js modules available at runtime in Vitest
    const path = await import("node:path");
    const dir = path.dirname(url.fileURLToPath(import.meta.url));
    const jsonPath = path.resolve(dir, "../../public/card-data.json");
    if (!fs.existsSync(jsonPath)) return null;
    return JSON.parse(fs.readFileSync(jsonPath, "utf8")) as CardData[];
  } catch {
    return null;
  }
}
/* eslint-enable @typescript-eslint/no-unsafe-assignment, @typescript-eslint/no-unsafe-call, @typescript-eslint/no-unsafe-member-access, @typescript-eslint/no-unsafe-argument */

describe("loadCardDatabase integration (real card-data.json)", () => {
  it("loads the generated card set and indexes it by cardNumber", async () => {
    const raw = await readCardDataJson();
    if (raw === null) return;

    vi.stubGlobal(
      "fetch",
      vi.fn().mockResolvedValue({
        ok: true,
        json: () => Promise.resolve(raw),
      }),
    );

    const db = await loadCardDatabase();
    expect(db.size).toBe(582);
  });

  it("every card has all required fields with correct types", async () => {
    const raw = await readCardDataJson();
    if (raw === null) return;

    vi.stubGlobal(
      "fetch",
      vi.fn().mockResolvedValue({
        ok: true,
        json: () => Promise.resolve(raw),
      }),
    );

    const db = await loadCardDatabase();
    const validCardTypes = new Set(["Character", "Event"]);
    const validRarities = new Set([
      "Common",
      "Uncommon",
      "Rare",
      "Legendary",
    ]);
    for (const [cardNumber, card] of db) {
      expect(cardNumber).toBe(card.cardNumber);
      expect(typeof card.name).toBe("string");
      expect(typeof card.id).toBe("string");
      expect(typeof card.cardNumber).toBe("number");
      expect(validCardTypes.has(card.cardType)).toBe(true);
      expect(typeof card.subtype).toBe("string");
      expect(validRarities.has(card.rarity)).toBe(true);
      expect(
        card.energyCost === null || typeof card.energyCost === "number",
      ).toBe(true);
      expect(
        card.spark === null || typeof card.spark === "number",
      ).toBe(true);
      expect(typeof card.isFast).toBe("boolean");
      expect(Array.isArray(card.tides)).toBe(true);
      expect(card.tides.length).toBeGreaterThan(0);
      for (const packageTideId of card.tides) {
        expect(typeof packageTideId).toBe("string");
        expect(packageTideId.length).toBeGreaterThan(0);
      }
      expect(typeof card.renderedText).toBe("string");
      expect(typeof card.imageNumber).toBe("number");
      expect(typeof card.artOwned).toBe("boolean");
    }
  });

  it("sample card lookup returns expected structure", async () => {
    const raw = await readCardDataJson();
    if (raw === null) return;

    vi.stubGlobal(
      "fetch",
      vi.fn().mockResolvedValue({
        ok: true,
        json: () => Promise.resolve(raw),
      }),
    );

    const db = await loadCardDatabase();
    const card = db.get(1);
    expect(card).toBeDefined();
    expect(card?.cardNumber).toBe(1);
    expect(card?.name.length).toBeGreaterThan(0);
    expect(card ? cardAccentTide(card) : null).not.toBeNull();
  });
});
